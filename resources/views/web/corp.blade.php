                            <div id="ctl00_PlaceHolderMain_ctl01_ctl01_label" style="display:none">Page Content</div>
                            <div id="ctl00_PlaceHolderMain_ctl01_ctl01__ControlWrapper_RichHtmlField" class="ms-rtestate-field" style="display:inline" aria-labelledby="ctl00_PlaceHolderMain_ctl01_ctl01_label">
                                <div class="blueFieldTitle">Corporate Governance Framework</div>
                                <div style="text-align:justify">Corporate Governance provides the structure through which the objectives of the Bank are set, and the means of attaining them and monitoring performance are defined and determined. Strong Corporate Governance requires having a clear and proper decision-making process that ensures the allocation of responsibility, management of conflicts of interests and an adequate internal control framework. It defines the rights of shareholders in addition to the rights and responsibilities attributed to the directors and managers and spells out the rules and procedures for making decisions on corporate affairs.<br></div>
                                <div style="text-align:justify"><br></div>
                                <div style="text-align:justify">Fransabank corporate governance framework is set out in the corporate governance code. The code delineates a corporate governance framework in line with the regulatory requirements and international best practices and sets the grounds for other governance policies, charters and codes such as the rights of shareholders’ policy, policy for managing conflicts of interest, disclosure policy, employees’ handbook, etc. All of these are regularly updated to cope with the evolution of the regulatory framework and to best serve the changing needs of stakeholders.</div>
                                <div style="text-align:justify">&nbsp;<br></div>
                                <div>
                                    <div class="ms-rteThemeForeColor-2-0"><span style="color:rgb(51, 51, 51);font-size:20px;font-weight:600">Governance Structure</span><br></div>
                                </div>
                                <div><br></div>
                                <div style="text-align:justify">Fransabank governance structure, which aims to provide an efficient framework for the assignment of responsibility and accountability, is designed in a way that facilitates a clearly defined decision-making process. It includes the General Assembly of shareholders; the Board of Directors; the Chairman; the Deputy Chairman; the various committees, control functions; the external auditors; general and senior management and the business and support functions.&nbsp;</div>
                                <div style="text-align:justify">&ZeroWidthSpace;<br></div>
                                <div style="text-align:justify"></div>
                                <div>
                                    <div>
                                        <div class="blueFieldTitle">Rights&nbsp;of Shareholders&nbsp;</div>
                                    </div>
                                    <div><span style="text-align:justify">Shareholders enjoy all rights conferred upon them by the Lebanese Code of Commerce, including the right to vote at the General Assembly, the right to receive dividends, the right to transfer their shares and the preferential right to subscribe to capital increases. All common shareholders, including minority shareholders, enjoy the same rights and benefits and have one voting right for each common share (the principle of one share, one vote) without limitation. Shareholders who own registered shares for at least two years are entitled to a double voting right according to Article 117 of the Lebanese Code of Commerce.&nbsp;<br></span></div>
                                    <div><span style="text-align:justify"><br></span></div>
                                    <div>
                                        <div>
                                            <div class="blueFieldTitle">Board of Directors</div>
                                        </div>
                                        <div style="text-align:justify">The Board of Directors is entrusted with the duty of ensuring the proper management of the Bank in the best interest of its shareholders, depositors, and other stakeholders, in accordance with applicable laws and regulations.&nbsp;</div>
                                        <div style="text-align:justify"><br></div>
                                        <div style="text-align:justify">A charter of the Board of Directors is developed in line with the prevailing Lebanese laws &amp; regulations and international good practices. The charter defines, among other things, the composition, roles and responsibilities and the authority of the Board of Directors.</div>
                                        <div style="text-align:justify"><br></div>
                                        <div style="text-align:justify">The Board has overall responsibility of the Bank, including adopting and overseeing the implementation of the Bank’s strategic objectives, risk strategy, risk policies, corporate governance and corporate values, as well as ensuring that adequate, effective and independent controls are in place. The Board also exercises adequate oversight over the Group entities and ensures that each entity of the Group adopts corporate governance policies and mechanisms appropriate to its structure, business and risks.&nbsp;</div>
                                        <div style="text-align:justify"><br></div>
                                        <div style="text-align:justify">The Board of Directors elected by the General Assembly of Shareholders for a renewable mandate of three years. The ‘Board Members Succession Plan’ clearly defines the criteria and conditions for the nomination and selection of Board members. Shareholders have the power, through the General Assembly, to remove Directors <em>ad nutum</em>, at any time and without cause, even before the expiration of their tenor.</div>
                                        <div style="text-align:justify"><br></div>
                                        <div style="text-align:justify">The Board consists of a mix of executive, non-executive and independent members. The majority of its members qualify as non-executive. This composition aims to safeguard the governance and effectiveness of the Board of Directors.&nbsp;</div>
                                        <div style="text-align:justify"><br></div>
                                        <div style="text-align:justify">During 2019, the Board of Directors has met four times.</div>
                                        <div style="text-align:justify"><br></div>
                                        <div><span style="text-align:justify"></span></div>
                                    </div>
                                </div>
                                <div>
                                    <div>
                                        <div>
                                            <div class="blueFieldTitle">Board Committees</div>
                                        </div>
                                        <div style="text-align:justify">In carrying out its oversight responsibilities, the Board is supported by the Corporate Governance Committee, Board Risk Committee, Audit Committee, the Remuneration Committee and Anti-Money Laundering / Combating the Financing of Terrorism (AML / CFT) Board Committee&nbsp;and Board Nomination Committee. Except for the latter, these&nbsp;committees are chaired by independent non-executive members.&nbsp;</div>
                                        <div style="text-align:justify"><br></div>
                                        <div style="text-align:justify">Each of the Board committees has its own charter that defines each committee’s scope of work, membership structure and composition, meetings as well as its roles and responsibilities. The charters are regularly updated to ensure compliance with local and international standards.</div>
                                        <div style="text-align:justify"><br></div>
                                        <div style="text-align:justify">The Corporate Governance Committee, Risk Committee, Audit Committee and AML / CFT Board Committee meet at least quarterly and when necessary. The Remuneration Committee is set to meet at least semi-annually&nbsp;and the Board Nomination Committee as need be..&nbsp;</div>
                                    </div>
                                </div>
                                <div><span style="text-align:justify">
                                        <div style="text-align:-webkit-left"><span style="text-align:justify"></span></div>
                                    </span></div>
                                <div><b style="color:rgb(50, 50, 50)"><br></b></div>
                                <div><b style="color:rgb(50, 50, 50)">Corporate Governance Committee</b><br></div>
                                <div><br></div>
                                <div><span style="text-align:justify">The responsibility of the Corporate Governance Committee is to provide oversight of all material corporate governance issues affecting the Bank and its subsidiaries and to ensure that Fransabank corporate governance practices are in line with the regulatory requirements and international best practices.<br></span></div>
                                <div>
                                    <div><strong><br></strong></div>
                                    <div><strong>Risk Committee</strong><br></div>
                                </div>
                                <div><strong><br></strong></div>
                                <div style="text-align:justify">
                                    <div>The Board Risk Committee’s responsibilities are to assist the Board of Directors in fulfilling its risk-related duties and to oversee the proper implementation of the risk management principles. In discharging its responsibilities, the committee monitors the Bank’s risk profile vis-à-vis its risk appetite through the reports submitted by the Group Chief Risk Officer to the Board Risk Committee prior to presenting them to the Board of Directors. The committee is also responsible for recommending to the Board of Directors the Bank’s risk policies including the risk appetite and risk tolerance.&nbsp;</div>
                                    <div>&nbsp;&nbsp;<br></div>
                                    <div></div>
                                </div>
                                <div>
                                    <div><strong>Audit Committee</strong></div>
                                </div>
                                <div><strong></strong>
                                    <font color="#323232"><b><br></b></font>The Audit Committee assists the Board of Directors in its oversight responsibilities regarding the:
                                </div>
                                <div><br></div>
                                <div>
                                    <div>
                                        <ul>
                                            <li>Evaluation of the internal control regulations and procedures<br></li>
                                            <li>Assessment of the qualifications and independence of the external auditors<br></li>
                                            <li>Supervision of the internal audit’s activities<br></li>
                                            <li>Integrity of the financial statements<br></li>
                                            <li>Review of the Bank’s disclosure standards<br></li>
                                        </ul>
                                    </div>
                                </div>
                                <div><br></div>
                                <div>
                                    <div>
                                        <div>
                                            <font color="#323232"><b>Remuneration&nbsp;</b></font><strong>Committee</strong>
                                        </div>
                                        <div>
                                            <div><br></div>
                                            <div>The responsibility of the Remuneration Committee is to ensure that the Bank has comprehensive remuneration policies and procedures. The Committee defines the remuneration system and submits it to the Board of Directors for approval. It also controls the proper implementation of the remuneration policy and reviews periodically (at least annually) the basic rules and principles of the Bank’s remuneration policy in order to ensure that the set objectives are attained.&nbsp;<br></div>
                                            <div></div>
                                        </div>
                                    </div>
                                    <div>
                                        <font color="#323232"><b><br></b></font>
                                    </div>
                                    <div>
                                        <div>
                                            <font color="#323232"><b>AML / CFT Board Committee&nbsp;</b></font>
                                        </div>
                                        <div>
                                            <div><br></div>
                                        </div>
                                        <div>The AML/CFT Board Committee supports the Board of Directors in exercising its supervisory mission and role, in the context of fighting money laundering and financing of terrorism, in understanding the relevant risks, and in helping the Board take appropriate decisions in this regard. The committee reviews and approves the AML/CFT Policy &amp; Procedures and the proper incentive required for its full implementation. It also examines the suspicious cases and transactions and takes appropriate decisions thereupon.<br></div>
                                        <font color="#323232"><b></b></font>
                                    </div>
                                    <div>
                                        <font color="#323232"><b><br></b></font>
                                    </div>
                                    <div>
                                        <div>
                                            <font color="#323232"><b>Board Nomination Committee&nbsp;</b><br></font>
                                        </div>
                                        <div><span style="text-align:justify"><br></span></div>
                                        <div style="text-align:justify">
                                            <div>The Board Nomination Committee assists the Board of Directors in reviewing membership applications to join the Board of Directors with the aim to meet the required qualifications criteria. The selected candidatures would therefrom be submitted to the Board of Directors, to be channelled thereafter to the General Assembly.<br></div>
                                            <div>&ZeroWidthSpace;<br></div>
                                        </div>
                                        <div style="text-align:justify"><b style="color:rgb(50, 50, 50)">Management</b></div>
                                    </div>
                                    <div><span style="text-align:justify"><br></span></div>
                                    <div style="text-align:justify">Senior management undertakes and manages the Bank’s activities under the direction and oversight of the Board. Members of senior management are responsible and are held accountable for overseeing the day-to-day management of the Bank.&nbsp;</div>
                                    <div style="text-align:justify"><br></div>
                                    <div style="text-align:justify">The Chairman of the Board may suggest to the Board the appointment of one or more General Managers and Deputy General Managers, who shall act for account and under the full responsibility of the Chairman.&nbsp;</div>
                                    <div style="text-align:justify"><br></div>
                                    <div style="text-align:justify">Specialized management committees are established, whose members include senior staff, having the responsibility to set strategies and take decisions as necessary for the development of the Bank’s activities and to ensure a proper management of any potential conflict of interest.&nbsp;</div>
                                    <div style="text-align:justify"><br></div>
                                    <div><span style="text-align:justify"></span></div>
                                </div>
                                <div style="text-align:justify">
                                    <div>
                                        <div>
                                            <font color="#323232"><b>Conflicts of Interest</b></font>
                                        </div>
                                        <div><br></div>
                                        <div>Fransabank has a policy for managing conflicts of interest that sets the ‘tone at the top’ in terms of the principles to abide by to prevent and manage any cases of conflicts of interest.</div>
                                        <div><br></div>
                                        <div>A conflict of interest is a situation where different parties have interests that conflict with each other. Such conflicts may arise between:<br></div>
                                        <div><br></div>
                                        <div><span style="background-color:initial">(i) the interests of customers on the one hand and the interests of the Bank or its related parties or its employees on the other,</span><br>
                                            <ol>
                                                <li><span></span></li>
                                            </ol>
                                            <span style="background-color:initial">(ii) the interests of a customer on the one hand and the interest of another custo</span><span style="background-color:initial">mer on the other.</span><span style="background-color:initial">&ZeroWidthSpace;</span><br>
                                        </div>
                                        <div><br></div>
                                        <div>The Bank will always strive to have in place (i) preventive measures to avoid operations that would create material conflicts of interest as well as (ii) an effective organizational and administrative framework, covering all hierarchical levels, for dealing with conflicts of interest.<br></div>
                                        <div></div>
                                        <div><br></div>
                                        <font color="#323232"><b></b></font>
                                    </div>
                                    <div>
                                        <font color="#323232"><b>Control Functions</b></font>
                                    </div>
                                    <div><br></div>
                                    <div>The internal control system is a set of rules and controls governing the Bank’s organisational and operational structure.&nbsp;</div>
                                    <div><br></div>
                                    <div>It is based on interrelated components, among which, the adoption of a clear and documented organizational structure, the assessment of risks, the adoption of systems for risk assessment &amp; monitoring, and a continuous monitoring process through reporting any identified weakness or any violation to the existing policies and procedures.&nbsp;</div>
                                    <div><br></div>
                                    <div>It is designed to ensure process integrity, compliance and effectiveness as well as provide reasonable assurance that financial and management information is reliable, timely and complete.</div>
                                    <div><br></div>
                                    <div>The Bank recognizes the importance of implementing a solid and sound structure for control functions, namely the risk management, compliance, internal audit and inspection functions, which shall ensure that the Bank’s activities are performed in accordance with the prevailing laws and regulations as well as with the Bank’s policies and procedures.</div>
